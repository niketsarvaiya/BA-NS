export function getProductsByCategory(category: string): ProductDictItem[] {
  return PRODUCT_DICTIONARY.filter((product) => product.category === category);
}

export function searchProducts(query: string): ProductDictItem[] {
  const lowerQuery = query.toLowerCase();
  return PRODUCT_DICTIONARY.filter(
    (product) =>
      product.name.toLowerCase().includes(lowerQuery) ||
      product.category.toLowerCase().includes(lowerQuery) ||
      product.subCategory?.toLowerCase().includes(lowerQuery) ||
      product.description?.toLowerCase().includes(lowerQuery)
  );
}

export function getAllCategories(): string[] {
  const categories = new Set(PRODUCT_DICTIONARY.map((p) => p.category));
  return Array.from(categories).sort();
}
